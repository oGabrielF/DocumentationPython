# VARIAVEIS

# Variaveis permitem o armazenamento de informações na memória.
# O nome da variavel não pode ter caracteres especias e nem espaço.
# Variavel é uma case sensitive ou seja: Nome_variable é diferente de nome_variable.
# [=] Operador de atribuição.
# Valor: define o tipo da variavel.
# Nome_variable = valor
my_variable = "Hello World"
print(my_variable)

# TIPOS DE DADOS

# Número inteiro = 47/34/54...
# Número flutuante = 12.67/56.63/23.43... Número decimais.
# Textual(String) = "Hello World"
# Booleana pode ser true or false.
varInteiro = 10
varFlutuante = 15.90
varString = "Hello World"
varBoolean = True
print(varInteiro, varFlutuante, varString, varBoolean)

