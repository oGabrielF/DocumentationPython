# AND : Comparar se duas condições são verdadeiras.
#  OR : Comparar se pelo menos uma condição seja verdadeira.
# NOT : Irá inverter o valor.
x = 3
y = 3
z = 3
print(x == y and x == z) # Irá retornar True pois as duas condições são verdadeiras.
a = 2 
b = 3 
c = 5
print(a == b and a == c) # Irá retornar False pois uma das condições é falsa.
d = 3
e = 3
f = 4
print(d == e or d == f) # Irá retornar True pois pelo menos uma das condições é verdadeira.

