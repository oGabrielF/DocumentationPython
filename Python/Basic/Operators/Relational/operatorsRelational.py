# OPERADORES RELACIONAIS
# Use esses operadores para fazer verificações

#  = : Recebe o valor
# == : Igual
# != : Diferente
#  > : Maior
#  < : Menor
# >= : Maior ou Igual
# <= : Menor ou Igual
x = 2
y = 3
print(x == y) # Irá retornar False pois X não é igual a Y
soma = x + y
print(soma == x) # Irá retornar False pois o resultado da soma é igual a 5 e não a X que no caso X = 3.
print(soma == 5) # Irá retornar True pois o resultado da soma é 5.
