# LAÇO DE REPETIÇÃO WHILE

# Estruturas de repetição
# Iteradores
# Repetem um trecho do código
#   Enquanto um condição avaliada for verdadeira
#   Durante uma pré-determinada condição
x = 1

while x < 10: # Essa linha se lê: (Enquanto x for menor que 10:)
    print(x)
    x = x + 1 # A cada rodada ele irá adicionar 1 ao valor de x
# Eu posso usar x += 1 que é a mais coisa que x = x + 1.

